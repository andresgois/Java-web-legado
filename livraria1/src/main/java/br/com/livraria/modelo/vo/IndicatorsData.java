package br.com.livraria.modelo.vo;

import java.util.Date;

public class IndicatorsData {
   
    private Date date;
    private String x;
    private float y;
    public Date getDate() {
        return date;
    }
    public void setDate(Date date) {
        this.date = date;
    }
    public String getX() {
        return x;
    }
    public void setX(String x) {
        this.x = x;
    }
    public float getY() {
        return y;
    }
    public void setY(float y) {
        this.y = y;
    }
    
    
}
